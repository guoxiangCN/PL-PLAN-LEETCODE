// @lc code=start
class Solution
{
public:
    int integerReplacement(int n)
    {
        if (n == 1)
            return 0;
        if (n == 2)
            return 1;
        if (n == 3)
            return 2;
        if (n == 4)
            return 2;
        
    }
};
